// --- 3. APP STATE ---
let activeProject = null;
let step = 0;
let currentLang = "es";

// --- 4. DOM ELEMENTS ---
const elements = {
  colBio: document.getElementById("colBio"),
  colDetail: document.getElementById("colDetail"),
  detailImg: document.getElementById("detailImg"),
  detailImgEl: document.getElementById("detailImgEl"),
  detailCounter: document.getElementById("detailCounter"),
  detailText: document.getElementById("detailText"),
  projectItems: document.querySelectorAll("[data-project]"),
  navLeft: document.getElementById("navLeft"),
  navRight: document.getElementById("navRight"),
};

// --- 5. CORE FUNCTIONS ---

function render() {
  const p = projects[activeProject];
  elements.navLeft.classList.add("active");
  elements.navRight.classList.add("active");
  if (!p) return;

  const totalSteps = p.images.length + 1;

  if (step < p.images.length) {
    // Show Images
    elements.detailImgEl.src = p.images[step];
    elements.detailImg.classList.add("visible");
    elements.detailText.classList.remove("visible");
  } else {
    // Show Text Slide
    elements.detailImg.classList.remove("visible");
    elements.detailText.classList.add("visible");
    // Uses innerHTML so your <br> tags work perfectly
    elements.detailText.innerHTML = p.text[currentLang];
  }

  // Update Counter (1/5, 2/5...)
  elements.detailCounter.classList.add("visible");
  elements.detailCounter.textContent = `${step + 1}/${totalSteps}`;
}

function closeProject() {
  activeProject = null;
  step = 0;

  // Reset the alignment
  elements.colDetail.style.paddingTop = "0px";

  elements.detailImg.classList.remove("visible");
  elements.detailText.classList.remove("visible");
  elements.detailCounter.classList.remove("visible");
  elements.projectItems.forEach((li) => li.classList.remove("active"));
  elements.navLeft.classList.remove("active");
  elements.navRight.classList.remove("active");
}

// --- 6. EVENT LISTENERS ---

// A. Click a project in the list
elements.projectItems.forEach((item) => {
  item.addEventListener("click", (e) => {
    e.stopPropagation();

    // --- NEW: PREVENT WEIRD SLIDE ON FIRST CLICK ---
    // If no project is active, we want it to appear instantly.
    if (!activeProject) {
      elements.colDetail.style.transition = "none";
    } else {
      // If one is already open, keep the smooth slide
      elements.colDetail.style.transition = "padding-top 0.4s ease-in-out";
    }

    elements.projectItems.forEach((li) => li.classList.remove("active"));
    item.classList.add("active");

    const listTop = item.parentElement.getBoundingClientRect().top;
    const itemTop = item.getBoundingClientRect().top;

    // Using your 8px offset for x-height alignment
    const opticalOffset = 8;
    const slideDistance = itemTop - listTop + opticalOffset;

    elements.colDetail.style.paddingTop = `${slideDistance}px`;

    activeProject = item.dataset.project;
    step = 0;
    render();
  });
});

// --- B. NAVEGACIÓN DIRECCIONAL (IZQUIERDA / DERECHA) ---

// Clic en la zona izquierda: va hacia atrás
elements.navLeft.addEventListener("click", (e) => {
  e.stopPropagation(); // Evita que el clic se propague
  if (!activeProject) return;

  const p = projects[activeProject];
  const totalSteps = p.images.length + 1;

  // Fórmula para ir atrás y que sea circular (del primero al último)
  step = (step - 1 + totalSteps) % totalSteps;
  render();
});

// Clic en la zona derecha: va hacia adelante
elements.navRight.addEventListener("click", (e) => {
  e.stopPropagation();
  if (!activeProject) return;

  const p = projects[activeProject];
  const totalSteps = p.images.length + 1;

  // Fórmula para ir adelante y que sea circular
  step = (step + 1) % totalSteps;
  render();
});

// C. Click outside to close (Bio y Lista ahora cierran el proyecto)
document.addEventListener("click", (e) => {
  // Solo queremos que se mantenga abierto si pulsas en el DETALLE o en el FOOTER
  const isDetail = e.target.closest("#colDetail");
  const isFooter = e.target.closest("footer");

  // Si el clic NO ha sido en el detalle Y el clic NO ha sido en el footer:
  if (!isDetail && !isFooter && activeProject) {
    closeProject();
  }
});

// D. Language selector logic
document.querySelectorAll("[data-lang]").forEach((button) => {
  button.addEventListener("click", (e) => {
    e.stopPropagation(); //

    currentLang = button.dataset.lang; // Now matches "en", "es", "val"

    // Update the Bio
    elements.colBio.textContent = uiStrings.bio[currentLang];

    if (activeProject) {
      render(); //
    }

    // --- NEW UNDERLINE LOGIC ---
    // 1. Remove the 'active-lang' class from ALL buttons
    document.querySelectorAll("[data-lang]").forEach((b) => {
      b.classList.remove("active-lang");
    });

    // 2. Add the 'active-lang' class ONLY to the one we clicked
    button.classList.add("active-lang");
  });
});
